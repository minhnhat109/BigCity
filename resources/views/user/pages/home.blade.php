@section('js')
    <script>
        $(document).ready(function() {
            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });

            function formatNumber(number) {
                return new Intl.NumberFormat('vi-VI', {
                    style: 'currency',
                    currency: 'VND'
                }).format(number);
            }
            loadListRoom()
            function loadListRoom() {
                $.ajax({
                    url: '/home/get-data-room',
                    type: 'get',
                    success: function(res) {
                        var room = res.data;
                        var content = '';
                        $.each(room, function(key, value) {
                            content += '<div class="col mb-5">'
                            content += '<div class="card h-100">'
                            content +='<img class="card-img-top" src="'+ value.image+'" alt="..." style="width: 367px; height: 244px;"/>'
                            content += '<div class="card-body p-4">'
                            content += '<div class="text-start">'
                            content += '<h5 class="fw-bolder">'+ value.name +'</h5>'
                            content += '<h5 class="text-start">Price: '+formatNumber(value.price)+'</h5>'
                            content += '<div class="">'
                            content += '<span class="text-start">'+ value.short_description +'</span>'
                            content += '</div>'
                            content += '</div>'
                            content += '</div>'
                            content +='<div class="card-footer p-4 pt-0 border-top-0 bg-transparent">'
                            content +='<div class="text-center"><a class="btn btn-outline-dark mt-auto" href="/home/room-detail/'+value.id+'">View options</a>'
                            content += '</div>'
                            content += '</div>'
                            content += '</div>'
                            content += '</div>'
                        });
                        $("#listRoom").html(content);
                    },
                });
            }
        });
    </script>
